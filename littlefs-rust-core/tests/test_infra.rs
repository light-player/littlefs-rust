// ── PRNG tests ──────────────────────────────────────────────────────────────

/// Verify first 10 values of test_prng(seed=1) match the C xorshift32 sequence.
/// Reference values generated by compiling and running the C test_prng function
/// (reference/runners/test_runner.c:568-577).
#[test]
fn test_prng_matches_c() {
    let expected: [u32; 10] = [
        270369, 67634689, 2647435461, 307599695, 2398689233, 745495504, 632435482, 435756210,
        2005365029, 2916098932,
    ];
    let mut state: u32 = 1;
    for (i, &exp) in expected.iter().enumerate() {
        let val = test_prng(&mut state);
        assert_eq!(
            val, exp,
            "PRNG mismatch at step {}: got {}, expected {}",
            i, val, exp
        );
    }
}

/// Verify test_prng is deterministic: same seed → same sequence.
#[test]
fn test_prng_deterministic() {
    let mut s1: u32 = 42;
    let mut s2: u32 = 42;
    for _ in 0..100 {
        assert_eq!(test_prng(&mut s1), test_prng(&mut s2));
    }
}

// ── Bad-block behavior tests ────────────────────────────────────────────────

/// Verify ProgError behavior: prog returns LFS_ERR_CORRUPT on bad block.
///
/// C: lfs_emubd_prog bad block check (lfs_emubd.c:358-362)
#[test]
fn test_badblock_behavior_prog_error() {
    let mut env = config_badblock_with_behavior(128, BadBlockBehavior::ProgError);
    init_badblock_context(&mut env);
    env.badblock_ram.set_bad_block(5);

    let data = [0xABu8; 16];
    let result = write_block_raw(&env.config as *const LfsConfig, 5, 0, &data);
    assert_eq!(
        result, LFS_ERR_CORRUPT,
        "ProgError should return LFS_ERR_CORRUPT on prog"
    );

    let result_ok = write_block_raw(&env.config as *const LfsConfig, 6, 0, &data);
    assert_eq!(result_ok, 0, "Non-bad block should prog successfully");
}

