/// Verify ProgNoop behavior: prog silently does nothing on bad block.
///
/// C: lfs_emubd_prog bad block check (lfs_emubd.c:363-369)
#[test]
fn test_badblock_behavior_prog_noop() {
    let mut env = config_badblock_with_behavior(128, BadBlockBehavior::ProgNoop);
    init_badblock_context(&mut env);

    // Erase block 5, then mark it bad
    unsafe {
        let erase = env.config.erase.expect("erase callback");
        erase(&env.config as *const LfsConfig, 5);
    }
    env.badblock_ram.set_bad_block(5);

    // Prog should succeed (return 0) but not actually write
    let data = [0xABu8; 16];
    let result = write_block_raw(&env.config as *const LfsConfig, 5, 0, &data);
    assert_eq!(result, 0, "ProgNoop should return 0");

    // Verify data was NOT written (should still be 0xFF from erase)
    let mut buf = [0u8; 16];
    let _ = common::read_block_raw(&env.config as *const LfsConfig, 5, 0, &mut buf);
    assert_eq!(buf, [0xFF; 16], "ProgNoop should not modify block data");
}

/// Verify EraseNoop behavior: erase silently does nothing on bad block,
/// and prog also silently does nothing (per C: ERASENOOP affects prog too).
///
/// C: lfs_emubd_erase bad block check (lfs_emubd.c:460-463)
/// C: lfs_emubd_prog bad block check (lfs_emubd.c:363-369)
#[test]
fn test_badblock_behavior_erase_noop() {
    let mut env = config_badblock_with_behavior(128, BadBlockBehavior::EraseNoop);
    init_badblock_context(&mut env);
    env.badblock_ram.set_bad_block(5);

    // Erase should succeed (return 0) but not actually erase
    let result = unsafe {
        let erase = env.config.erase.expect("erase callback");
        erase(&env.config as *const LfsConfig, 5)
    };
    assert_eq!(result, 0, "EraseNoop should return 0");

    // Prog should also noop (C: ERASENOOP makes prog noop too)
    let data = [0xABu8; 16];
    let result = write_block_raw(&env.config as *const LfsConfig, 5, 0, &data);
    assert_eq!(result, 0, "EraseNoop should make prog return 0 too");
}

// ── Wear-leveling BD tests ──────────────────────────────────────────────────

/// Verify erase fails after cycle limit.
///
/// C: lfs_emubd_erase wear tracking (lfs_emubd.c:453-469)
#[test]
fn test_wear_leveling_bd_exhaustion() {
    let erase_cycles = 5u32;
    let mut env = config_with_wear_leveling(128, erase_cycles);
    init_wear_leveling_context(&mut env);

    // Erase block 3 exactly erase_cycles times — should all succeed
    for i in 0..erase_cycles {
        let result = unsafe {
            let erase = env.config.erase.expect("erase callback");
            erase(&env.config as *const LfsConfig, 3)
        };
        assert_eq!(result, 0, "Erase #{} should succeed", i);
        assert_eq!(env.bd.get_wear(3), i + 1);
    }

    // Next erase should fail (ProgError is default behavior, but ERASEERROR
    // only triggers on erase for EraseError. Default is ProgError which
    // does NOT fail on erase — it fails on prog. So erase itself succeeds
    // for ProgError but doesn't increment wear.)
    // Actually per the C code, when worn and behavior is ProgError,
    // erase falls through to the normal path. Let me re-check...
    //
    // C erase path: if worn → check behavior. Only EraseError/EraseNoop have
    // special handling. For ProgError/ReadError/ProgNoop, erase falls through
    // (no increment since worn block path doesn't increment).
    // So with default ProgError, erase on worn block just erases normally
    // (no increment, no error).
    let result = unsafe {
        let erase = env.config.erase.expect("erase callback");
        erase(&env.config as *const LfsConfig, 3)
    };
    assert_eq!(
        result, 0,
        "ProgError behavior: erase on worn block should still succeed"
    );
    assert_eq!(
        env.bd.get_wear(3),
        erase_cycles,
        "Wear should not increment past limit"
    );

    // But prog should fail with ProgError
    let data = [0xABu8; 16];
    let result = write_block_raw(&env.config as *const LfsConfig, 3, 0, &data);
    assert_eq!(
        result, LFS_ERR_CORRUPT,
        "ProgError: prog on worn block should fail"
    );
}

/// Verify EraseError behavior on wear-leveling BD.
#[test]
fn test_wear_leveling_bd_erase_error() {
    let erase_cycles = 3u32;
    let mut env =
        common::config_with_wear_leveling_behavior(128, erase_cycles, BadBlockBehavior::EraseError);
    init_wear_leveling_context(&mut env);

    // Erase 3 times (should succeed, incrementing wear each time)
    for _ in 0..erase_cycles {
        let result = unsafe {
            let erase = env.config.erase.expect("erase callback");
            erase(&env.config as *const LfsConfig, 2)
        };
        assert_eq!(result, 0);
    }
    assert_eq!(env.bd.get_wear(2), erase_cycles);

    // Next erase should fail
    let result = unsafe {
        let erase = env.config.erase.expect("erase callback");
        erase(&env.config as *const LfsConfig, 2)
    };
    assert_eq!(
        result, LFS_ERR_CORRUPT,
        "EraseError: erase on worn block should fail"
    );
}

/// Verify set_wear allows pre-configuring block wear.
#[test]
fn test_wear_leveling_bd_set_wear() {
    let mut env = config_with_wear_leveling(128, 10);
    init_wear_leveling_context(&mut env);

    env.bd.set_wear(5, 9);
    assert_eq!(env.bd.get_wear(5), 9);
    assert!(!env.bd.is_worn(5), "9 < 10, not worn yet");

    env.bd.set_wear(5, 10);
    assert!(env.bd.is_worn(5), "10 >= 10, should be worn");
}

// ── Write/verify PRNG file test ─────────────────────────────────────────────

/// Round-trip test: write PRNG data to a file, close, reopen, verify.
#[test]
fn test_write_verify_prng_file() {
    let mut env = default_config(128);
    init_context(&mut env);

    let mut lfs = core::mem::MaybeUninit::<littlefs_rust_core::Lfs>::zeroed();
    assert_ok(littlefs_rust_core::lfs_format(
        lfs.as_mut_ptr(),
        &env.config as *const LfsConfig,
    ));
    assert_ok(littlefs_rust_core::lfs_mount(
        lfs.as_mut_ptr(),
        &env.config as *const LfsConfig,
    ));

    let path = common::path_bytes("prng_test");
    let mut file = core::mem::MaybeUninit::<littlefs_rust_core::LfsFile>::zeroed();

    // Write 256 bytes in 31-byte chunks with seed=1
    assert_ok(littlefs_rust_core::lfs_file_open(
        lfs.as_mut_ptr(),
        file.as_mut_ptr(),
        path.as_ptr(),
        LFS_O_WRONLY | LFS_O_CREAT,
    ));
    write_prng_file(lfs.as_mut_ptr(), file.as_mut_ptr(), 256, 31, 1);
    assert_ok(littlefs_rust_core::lfs_file_close(
        lfs.as_mut_ptr(),
        file.as_mut_ptr(),
    ));
    assert_ok(littlefs_rust_core::lfs_unmount(lfs.as_mut_ptr()));

    // Remount and verify
    assert_ok(littlefs_rust_core::lfs_mount(
        lfs.as_mut_ptr(),
        &env.config as *const LfsConfig,
    ));
    assert_ok(littlefs_rust_core::lfs_file_open(
        lfs.as_mut_ptr(),
        file.as_mut_ptr(),
        path.as_ptr(),
        LFS_O_RDONLY,
    ));
    verify_prng_file(lfs.as_mut_ptr(), file.as_mut_ptr(), 256, 31, 1);
    assert_ok(littlefs_rust_core::lfs_file_close(
        lfs.as_mut_ptr(),
        file.as_mut_ptr(),
    ));
    assert_ok(littlefs_rust_core::lfs_unmount(lfs.as_mut_ptr()));
}
